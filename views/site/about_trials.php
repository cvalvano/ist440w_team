            <h3>HIPAA</h3>
            <p>
                The Health Insurance Portability and Accountability Act of 1996 (HIPAA; Pub.L. 104–191, 110 Stat. 1936,
                enacted August 21, 1996) was enacted by the United States Congress and signed by President Bill Clinton
                in 1996. It has been known as the Kennedy–Kassebaum Act or Kassebaum-Kennedy Act after two of its leading
                sponsors. Title I of HIPAA protects health insurance coverage for workers and their families when
                they change or lose their jobs. Title II of HIPAA, known as the Administrative Simplification (AS) provisions,
                requires the establishment of national standards for electronic health care transactions and national
                identifiers for providers, health insurance plans, and&nbsp;employers.
            </p>
            <p>
                For more information, <a href="https://en.wikipedia.org/wiki/Health_Insurance_Portability_and_Accountability_Act" target="_blank">click here</a>.
