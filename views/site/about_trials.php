            <h3>Informed Consent</h3>
            <p>
                Informed consent is a process for getting permission before conducting a healthcare intervention on a person.
                A health care provider may ask a patient to consent to receive therapy before providing it, or a clinical
                researcher may ask a research participant before enrolling that person into a clinical trial. Informed consent
                is collected according to guidelines from the fields of medical ethics and research&nbsp;ethics.
            </p>
            <p>
                An informed consent can be said to have been given based upon a clear appreciation and understanding of the
                facts, implications, and consequences of an action. To give informed consent, the individual concerned must
                have adequate reasoning faculties and be in possession of all relevant facts. Impairments to reasoning and
                judgment that may prevent informed consent include basic intellectual or emotional immaturity, high levels
                of stress such as PTSD or a severe intellectual disability, severe mental illness, intoxication, severe sleep
                deprivation, Alzheimer's disease, or being in&nbsp;a&nbsp;coma.
            </p>
            <p>
                Some acts can take place because of a lack of informed consent. In cases where an individual is considered
                unable to give informed consent, another person is generally authorized to give consent on his behalf, e.g.,
                parents or legal guardians of a child (though in this circumstance the child may be required to provide
                informed assent) and conservators for the mentally&nbsp;ill.
            </p>
            <p>
                In cases where an individual is provided insufficient information to form a reasoned decision, serious ethical
                issues arise. Such cases in a clinical trial in medical research are anticipated and prevented by an ethics
                committee or Institutional Review&nbsp;Board.
            </p>
            <p>
                Informed Consent Form Templates can be found on the <a target="_blank" href="//www.who.int/rpc/research_ethics/informed_consent/en/">World Health Organization Website</a>
                for practical&nbsp;use.
            </p>
            <p>
                For more information, <a href="https://en.wikipedia.org/wiki/Informed_consent" target="_blank">click here</a>.
            </p>
