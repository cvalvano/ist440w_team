            <h3>Good Clinical Practice (GCP)</h3>
            <p>
                Good clinical practice (GCP) is an international quality standard that is provided by ICH, an international
                body that defines standards, which governments can transpose into regulations for clinical trials involving
                human subjects. A similar guideline for clinical trials of medical devices is the international standard
                ISO 14155, that is valid in the European Union as a harmonized standard. These standards for clinical
                trials are sometimes referred to as ICH-GCP or ISO-GCP to differentiate between the two and the lowest grade
                of recommendation in clinical&nbsp;guidelines.
            </p>
            <p>
                GCP follows the International Conference on Harmonisation (ICH) of GCP guidelines. GCP enforces tight guidelines
                on ethical aspects of a clinical study. High standards are required in terms of comprehensive documentation
                for the clinical protocol, record keeping, training, and facilities, including computers and software. Quality
                assurance and inspections ensure that these standards are achieved. GCP aims to ensure that the studies are
                scientifically authentic and that the clinical properties of the investigational product are properly documented.
                Ongoing research shows that whether conducting research involving a new drug, a behavioral intervention, or
                an interview or survey, GCP provides investigators and their study teams with the tools to protect human
                subjects and collect quality&nbsp;data.
            </p>
            <p>
                GCP guidelines include protection of human rights for the subjects and volunteers in a clinical trial. It
                also provides assurance of the safety and efficacy of the newly developed&nbsp;compounds.
            </p>
            <p>
                GCP guidelines include standards on how clinical trials should be conducted, define the roles and responsibilities
                of clinical trial sponsors, clinical research investigators, and monitors. In the pharmaceutical industry
                monitors are often called Clinical Research&nbsp;Associates.
            </p>
            <p>
                For more information, <a href="https://en.wikipedia.org/wiki/Good_clinical_practice" target="_blank">click here</a>.
            </p>
