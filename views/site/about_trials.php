            <h3>Institutional Review Boards (IRB)</h3>
            <p>
                An institutional review board (IRB), also known as an independent ethics committee (IEC), ethical review
                board (ERB), or research ethics board (REB), is a committee that has been formally designated to approve,
                monitor, and review biomedical and behavioral research involving humans. They often conduct some form of
                risk-benefit analysis in an attempt to determine whether or not research should&nbsp;be&nbsp;done
            </p>
            <p>
                The purpose of the review process is to assure, both in advance and by periodic review, that appropriate
                steps are taken to protect the rights and welfare of humans participating as subjects in a research study.
                A key goal of IRBs is to protect human subjects from physical or psychological harm, which they attempt to
                do by reviewing research protocols and related materials. The protocol review assesses the ethics of the
                research and its methods, promotes fully informed and voluntary participation by prospective subjects
                capable of making such choices (or, if that is not possible, informed permission given by a suitable proxy),
                and seeks to maximize the safety of&nbsp;subjects.
            </p>
            <p>
                In the United States, the Food and Drug Administration (FDA) and Department of Health and Human Services
                (specifically Office for Human Research Protections) regulations have empowered IRBs to approve, require
                modifications in planned research prior to approval, or disapprove research. IRBs are responsible for
                critical oversight functions for research conducted on human subjects that are "scientific," "ethical,"
                and&nbsp;"regulatory."
            </p>
            <p>
                IRBs are most commonly used for studies in the fields of health and the social sciences, including anthropology,
                sociology, and psychology. Such studies may be clinical trials of new drugs or devices, studies of personal
                or social behavior, opinions or attitudes, or studies of how health care is delivered and might be&nbsp;improved.
            </p>
            <p>
                For more information, <a href="https://en.wikipedia.org/wiki/Institutional_review_board" target="_blank">click here</a>.
            </p>
