            <h3>Phases in Trials</h3>
            <div class="section group">
                <div class="col span_2_of_12 right"><h4 class="orange">Phase 0</h4></div>
                <div class="col span_10_of_12">
                    <p>
                        Phase 0 trials are the first-in-human trials. Single subtherapeutic doses of the study drug or treatment
                        are given to a small number of subjects (10 to 15) to gather preliminary data on the agent's pharmacodynamics
                        (what the drug does to the body) and pharmacokinetics (what the body does to the drugs). For a test drug,
                        the trial documents the absorption, distribution, metabolization, and removal (excretion) of the drug, and
                        the drug's interactions within the body, to confirm that these appear to be as&nbsp;expected.
                    </p>
                </div>
            </div>
            <div class="section group">
                <div class="col span_2_of_12 right"><h4 class="orange">Phase 1</h4></div>
                <div class="col span_10_of_12">
                    <p>
                        Testing within a small group of people (20–80) to evaluate safety, determine safe dosage ranges, and begin
                        to identify side effects. A drug's side effects could be subtle or long term, or may only happen with a
                        few people, so phase 1 trials are not expected to identify all side&nbsp;effects.
                    </p>
                </div>
            </div>
            <div class="section group">
                <div class="col span_2_of_12 right"><h4 class="orange">Phase 2</h4></div>
                <div class="col span_10_of_12">
                    <p>
                        Testing with a larger group of people (100–300) to see if it is effective and to further evaluate its safety.
                        The gradual increase in test group size allows less-common side effects to be progressively&nbsp;sought.
                    </p>
                </div>
            </div>
            <div class="section group">
                <div class="col span_2_of_12 right"><h4 class="orange">Phase 3</h4></div>
                <div class="col span_10_of_12">
                    <p>
                        Testing with large groups of people (1,000–3,000) to confirm its effectiveness, monitor side effects, compare
                        it to commonly used treatments, and collect information that will allow it to be used&nbsp;safely.
                    </p>
                </div>
            </div>
            <div class="section group">
                <div class="col span_2_of_12 right"><h4 class="orange">Phase 4</h4></div>
                <div class="col span_10_of_12">
                    <p>
                        Postmarketing studies delineate additional information, including the treatment's risks, benefits, and optimal
                        use. As such, they are ongoing during the drug's lifetime of active medical use. (Particularly relevant after
                        approval under FDA Accelerated Approval&nbsp;Program)
                    </p>
                </div>
            </div>
