            <h3>What are Clinical Trials?</h3>
            <p>
                Clinical trials are experiments done in clinical research. Such prospective biomedical or behavioral research
                studies on human participants are designed to answer specific questions about biomedical or behavioral
                interventions, including new treatments (such as novel vaccines, drugs, dietary choices, dietary supplements,
                and medical devices) and known interventions that warrant further study and comparison. Clinical trials
                generate data on safety and efficacy. They are conducted only after they have received health authority/ethics
                committee approval in the country where approval of the therapy is sought. These authorities are responsible
                for vetting the risk/benefit ratio of the trial - their approval does not mean that the therapy is 'safe'
                or effective, only that the trial may be&nbsp;conducted.
            </p>
            <p>
                Depending on product type and development stage, investigators initially enroll volunteers and/or patients
                into small pilot studies, and subsequently conduct progressively larger scale comparative studies. Clinical
                trials can vary in size and cost, and they can involve a single research center or multiple centers, in one
                country or in multiple countries. Clinical study design aims to ensure the scientific validity and
                reproducibility of the&nbsp;results.
            </p>
            <p>
                Trials can be quite costly, depending on a number of factors. The sponsor may be a governmental organization
                or a pharmaceutical, biotechnology or medical device company. Certain functions necessary to the trial,
                such as monitoring and lab work, may be managed by an outsourced partner, such as a contract research
                organization or a central laboratory.
            </p>
            <p>
                Only 10% of all drugs started in human clinical trials become an approved drug.
            </p>
            <p>
                For more information, <a href="https://en.wikipedia.org/wiki/Clinical_trial" target="_blank">click here</a>.
            </p>
